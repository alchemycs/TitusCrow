/**
 * Recurring events can be expressed by combining vaious temporal expressions.
 * The ITCTemporalExpression provides the common interface for these expressions.
 *
 * New component and composite temporal expressions can be easily created  by
 * implementing this interface
 *
 * @package TitusCrow
 * @subpackage TemporalExpression
 * @author  Michael McHugh <alchemist@alchemycs.net.au>
 * @copyright Michael McHugh, 2011
 * @license http://www.gnu.org/licenses/gpl.html GNU General Public License
 */
interface ITCTemporalExpression {

  /**
   * Determine if the temporal expression includes a certain date
   *
   * @param TCDate $aDate The date to test
   * @return bool TRUE if the temporal expression includes the date, FALSE otherwsise
   */
  public function includes(TCDate $aDate);

}

